class Solution {
public:
    int searchInsert(vector<int>& nums, int target) {
        int l = 0;
        int r = nums.size() - 1;

        while (l <= r) {
            int mid = l + (r - l) / 2;
            if (nums[mid] == target) {
                return mid;
            } else if (nums[mid] < target) {
                l = mid + 1;
            } else {
                r = mid - 1;
            }
        }
        return l;
        
    }
};

int main() {
    fast();

    Solution sol;

    vector<int> nums = {1,3,5,6};
    int target = 5;

    cout << sol.searchInsert(nums, target) << "\n"; // 2

    target = 2;

    cout << sol.searchInsert(nums, target) << "\n"; // 1

    target = 7;

    cout << sol.searchInsert(nums, target) << "\n";

    return 0;
}

